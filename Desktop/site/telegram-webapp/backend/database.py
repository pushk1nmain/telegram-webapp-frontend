def get_db_info() -> dict:
    """–ü–æ–ª—É—á–µ–Ω–∏–µ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ –±–∞–∑–µ –¥–∞–Ω–Ω—ã—Ö"""
    try:
        with engine.connect() as conn:
            # –í–µ—Ä—Å–∏—è PostgreSQL
            version_result = conn.execute(text("SELECT version()"))
            version = version_result.scalar()
            
            # –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–π
            connections_result = conn.execute(text(
                "SELECT count(*) FROM pg_stat_activity WHERE state = 'active'"
            ))
            active_connections = connections_result.scalar()
            
            # –†–∞–∑–º–µ—Ä –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö
            db_name = settings.DATABASE_URL_SYNC.split('/')[-1]
            size_result = conn.execute(text(
                f"SELECT pg_size_pretty(pg_database_size('{db_name}'))"
            ))
            db_size = size_result.scalar()
            
            return {
                "version": version,
                "active_connections": active_connections,
                "database_size": db_size,
                "status": "connected"
            }
    except Exception as e:
        logger.error(f"‚ùå –û—à–∏–±–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ –ë–î: {e}")
        return {"status": "error", "error": str(e)}

